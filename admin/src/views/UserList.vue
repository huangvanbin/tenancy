<template>
	<div>
		<h1 style="margin-top:0px;">用户列表</h1>
		<el-table :data="items.slice((currentPage - 1) * pagesize, currentPage * pagesize)" height="450" style="width: 100%">
			<el-table-column prop="_id" label="ID" width="240"></el-table-column>
			<el-table-column prop="username" label="用户名"></el-table-column>
			<!-- <el-table-column prop="password" label="密码"></el-table-column> -->
			<el-table-column prop="usericon" label="用户头像">
				<template slot-scope="scope">
				  <img :src="scope.row.usericon" style="height:3rem">
				</template>
			</el-table-column>
			
			<el-table-column label="操作" fixed="right" width="180">
			 <template slot-scope="scope">
				 <el-button size="small" @click="$router.push(`/users/edit/${scope.row._id}`)">编辑</el-button>
				 <el-button size="small" type="danger" @click="remove(scope.row)">删除</el-button>
			 </template>
			</el-table-column>
		</el-table>
		<el-pagination
			background
			@size-change="handleSizeChange"
			@current-change="handleCurrentChange"
			:current-page="currentPage"
			:page-sizes="[5, 10, 20, 40]" 
			:page-size="pagesize"         
			layout="total, sizes, prev, pager, next, jumper"
			:total="items.length">    //这是显示总共有多少数据，
		</el-pagination>
	</div>
</template>
<style>
	.el-table-column {
		width:120px;
	}
</style>
<script>
	export default {
		data() {
			return {
				items: [],
				currentPage: 1, //初始页
				pagesize: 5,    //    每页的数据
			}
		},
		methods:{
			handleSizeChange: function (size) {
				this.pagesize = size;
			},
			handleCurrentChange: function(currentPage){
				this.currentPage = currentPage;
			},
			async fetch(){
				const res = await this.$http.get(`rest/users`)
				this.items = res.data
			},
			async remove(row){
				this.$confirm(`是否确定要删除"${row.username}"这个用户?`, '提示', {
				confirmButtonText: '确定',
				cancelButtonText: '取消',
				type: 'warning'
			}).then(async() => {
				await this.$http.delete(`rest/users/${row._id}`)
				this.$message({
					type: 'success',
					message: '删除成功!'
				});
				this.fetch() 
			});
			}
		},
	 created(){
		 this.fetch();
		}
	}
</script>